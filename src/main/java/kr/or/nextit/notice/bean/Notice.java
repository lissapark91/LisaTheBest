package kr.or.nextit.notice.bean;

public class Notice{
	
	private int noti_seq_no     ;
	private String noti_mem_id     ;
	private String noti_title      ;
	private String noti_content    ;
	private String noti_reg_date   ;
	private String noti_upd_date   ;
	
	public int getNoti_seq_no() {
		return noti_seq_no;
	}
	public void setNoti_seq_no(int noti_seq_no) {
		this.noti_seq_no = noti_seq_no;
	}
	public String getNoti_mem_id() {
		return noti_mem_id;
	}
	public void setNoti_mem_id(String noti_mem_id) {
		this.noti_mem_id = noti_mem_id;
	}
	public String getNoti_title() {
		return noti_title;
	}
	public void setNoti_title(String noti_title) {
		this.noti_title = noti_title;
	}
	public String getNoti_content() {
		return noti_content;
	}
	public void setNoti_content(String noti_content) {
		this.noti_content = noti_content;
	}
	public String getNoti_reg_date() {
		return noti_reg_date;
	}
	public void setNoti_reg_date(String noti_reg_date) {
		this.noti_reg_date = noti_reg_date;
	}
	public String getNoti_upd_date() {
		return noti_upd_date;
	}
	public void setNoti_upd_date(String noti_upd_date) {
		this.noti_upd_date = noti_upd_date;
	}
	

	
	
	
}
